// code-from-spec: ROOT/tech_design/internal/frontmatter@v32

// Package frontmatter reads and parses YAML frontmatter from spec nodes,
// test nodes, and external dependency files.
//
// It reads line by line and stops as soon as the closing "---" delimiter is
// found, so the file body is never read.
package frontmatter

import (
	"bufio"
	"errors"
	"fmt"
	"os"
	"strings"

	"github.com/goccy/go-yaml"
)

// DependsOn represents a single entry in the depends_on list.
// LogicalName is the "path" YAML key; Version is the known version of that dependency.
type DependsOn struct {
	LogicalName string
	Version     int
}

// Frontmatter holds the parsed frontmatter fields from a spec node,
// test node, or external dependency file.
type Frontmatter struct {
	// Version is the node's own version. Required; ParseFrontmatter returns
	// ErrMissingVersion if this field is absent or zero.
	Version int

	// ParentVersion is the version of the parent node this node was written
	// against. Nil if the field is absent (e.g. root node, test nodes).
	ParentVersion *int

	// SubjectVersion is the version of the subject node a test node was written
	// against. Nil if the field is absent (non-test nodes).
	SubjectVersion *int

	// DependsOn lists cross-tree dependencies declared in the frontmatter.
	DependsOn []DependsOn

	// Implements lists output source files generated by this node.
	Implements []string
}

// Sentinel errors. All errors returned by ParseFrontmatter wrap one of these
// so callers can use errors.Is().
var (
	// ErrRead is returned when the file cannot be opened or read.
	ErrRead = errors.New("error reading file")

	// ErrFrontmatterParse is returned when the YAML frontmatter block is malformed.
	ErrFrontmatterParse = errors.New("error parsing frontmatter")

	// ErrFrontmatterMissing is returned when no "---" delimiters are found at
	// the top of the file.
	ErrFrontmatterMissing = errors.New("frontmatter not found")

	// ErrMissingVersion is returned when the version field is absent or zero.
	ErrMissingVersion = errors.New("version field is required")
)

// rawDependsOn mirrors the YAML structure of a single depends_on entry.
type rawDependsOn struct {
	Path    string `yaml:"path"`
	Version int    `yaml:"version"`
}

// rawFrontmatter mirrors the complete YAML frontmatter block for unmarshalling.
// Unknown fields are silently ignored by goccy/go-yaml.
type rawFrontmatter struct {
	Version        int            `yaml:"version"`
	ParentVersion  *int           `yaml:"parent_version"`
	SubjectVersion *int           `yaml:"subject_version"`
	DependsOn      []rawDependsOn `yaml:"depends_on"`
	Implements     []string       `yaml:"implements"`
}

// ParseFrontmatter reads the file at filePath, extracts the YAML frontmatter
// block (the content between the first "---" and the second "---" at the top
// of the file), parses it, and returns the result.
//
// The file is read line by line and parsing stops as soon as the closing "---"
// is found — the file body is never read.
//
// All returned errors wrap one of the sentinel variables (ErrRead,
// ErrFrontmatterParse, ErrFrontmatterMissing, ErrMissingVersion) so callers
// can use errors.Is() to distinguish them.
func ParseFrontmatter(filePath string) (*Frontmatter, error) {
	f, err := os.Open(filePath)
	if err != nil {
		return nil, fmt.Errorf("%w: %s: %v", ErrRead, filePath, err)
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)

	// Advance to the opening "---" delimiter. The opening delimiter must appear
	// at the very top of the file (nothing before it).
	foundOpen := false
	for scanner.Scan() {
		line := scanner.Text()
		if line == "---" {
			foundOpen = true
			break
		}
		// Any non-empty, non-delimiter content before the first "---" means
		// there is no frontmatter.
		if strings.TrimSpace(line) != "" {
			break
		}
	}
	if scanErr := scanner.Err(); scanErr != nil {
		return nil, fmt.Errorf("%w: %s: %v", ErrRead, filePath, scanErr)
	}
	if !foundOpen {
		return nil, fmt.Errorf("%w: %s", ErrFrontmatterMissing, filePath)
	}

	// Collect lines between the opening "---" and the closing "---".
	var lines []string
	foundClose := false
	for scanner.Scan() {
		line := scanner.Text()
		if line == "---" {
			foundClose = true
			break
		}
		lines = append(lines, line)
	}
	if scanErr := scanner.Err(); scanErr != nil {
		return nil, fmt.Errorf("%w: %s: %v", ErrRead, filePath, scanErr)
	}
	if !foundClose {
		// A second "---" was never encountered.
		return nil, fmt.Errorf("%w: %s", ErrFrontmatterMissing, filePath)
	}

	// Parse the collected YAML content.
	yamlContent := strings.Join(lines, "\n")
	var raw rawFrontmatter
	if unmarshalErr := yaml.Unmarshal([]byte(yamlContent), &raw); unmarshalErr != nil {
		return nil, fmt.Errorf("%w: %s: %v", ErrFrontmatterParse, filePath, unmarshalErr)
	}

	// version is required; zero means the field was absent or explicitly zero.
	if raw.Version == 0 {
		return nil, fmt.Errorf("%w: %s", ErrMissingVersion, filePath)
	}

	// Map raw YAML structs to the public Frontmatter type.
	result := &Frontmatter{
		Version:        raw.Version,
		ParentVersion:  raw.ParentVersion,
		SubjectVersion: raw.SubjectVersion,
		Implements:     raw.Implements,
	}
	for _, dep := range raw.DependsOn {
		result.DependsOn = append(result.DependsOn, DependsOn{
			LogicalName: dep.Path,
			Version:     dep.Version,
		})
	}

	return result, nil
}
